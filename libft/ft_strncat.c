#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s1 && s1[i])
		i++;
	while (s2 && s2[j] && j < n)
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	return (s1);
}
